package com.spacegame.core;

/**
 * The ButtonType enum represents the different types of buttons in the game. It is used to
 * determine the action to perform when a button is clicked.
 */
public enum ButtonType {
  /** Represents the pause button. When clicked, the game toggles its Pause-State. */
  TOGGLE_PAUSE,
  RESET_GAME,
  DEBUG_BUTTON,
}
